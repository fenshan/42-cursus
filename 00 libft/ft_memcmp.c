#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (n--)
		if (*(UC *)s1++ != *(UC *)s2++)
			return (*(UC *)--s1 - *(UC *)--s2);
	return (0);
}
